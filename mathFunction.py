from numpy import *

a1 = array([1,2,3,4])
a2 = array([5,6,7,8])

print(f"a1: {a1}, a2: {a2}")
print(f"a1+a2: {a1+a2}")
print(f"a1-2: {a1-2}")
print(f"sin(a1): {sin(a1)}")
print(f"log(a1): {log(a1)}")
print(f"mean(a1): {mean(a1)}")
print(f"min(a1): {min(a1)}")
print(f"max(a1): {max(a1)}")
print(f"sum(a1): {sum(a1)}")

